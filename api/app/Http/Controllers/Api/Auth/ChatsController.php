    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        try {
            $input = $request->all();

            $chats = Chats::find($id);

           $chats->trip_id = $input['trip_id'];$chats->renter_id = $input['renter_id'];$chats->owner_id = $input['owner_id'];$chats->created_at = $input['created_at'];$chats->updated_at = $input['updated_at'];

            $res = $chats->update();
            if ($res) {
                return response([
                    'status' => 'success',
                    'code' => 1,
                    'data' => $chats
                ], 200);
            }
            return response([
